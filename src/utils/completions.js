/**
 * Get completion source for a given language
 * @param {string} language - File extension or language name
 * @returns {import('@codemirror/autocomplete').CompletionSource}
 */
export function getCompletionSource(language) {
  const words = COMPLETIONS[language] || COMPLETIONS.default;
  return completeFromList(words);
}

/**
 * Get completion list for a given language
 * @param {string} language - File extension or language name
 * @returns {Array}
 */
export function getCompletions(language) {
  return COMPLETIONS[language] || COMPLETIONS.default;
}